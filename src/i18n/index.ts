interface I18nState {
  language: 'en' | 'ny';
  setLanguage: (lang: 'en' | 'ny') => void;
  t: (key: keyof typeof translations['en']) => string;
}

export const useI18nStore = create<I18nState>((set, get) => ({
  language: (localStorage.getItem('shopeasy_language') as 'en' | 'ny') || 'en',
  setLanguage: (lang) => {
    localStorage.setItem('shopeasy_language', lang);
    set({ language: lang });
  },
  t: (key) => {
    const lang = get().language;
    return translations[lang][key] || translations['en'][key] || String(key);
  }
}));
